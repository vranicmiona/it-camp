// 1. Aritmeticki operatori(Aritmetic operators);
// 1.1  +   operator 
console.log(4 + 5);
// 1.2 - odeuzimanje
console.log(4 - 5);
// 1.3 * mnozenje
console.log(4 * 5);
// 1.4 / deljenje
console.log(4 / 5);
// 1.5 ** operator stepenovanja
console.log(2 ** 3);
// 1.6 % Modulo (ostatak pri deljenju)
console.log(11 % 3); // 2
console.log(12 % 3); // 0

// Uslov za parnost:
// i % 2 = 0
// Uslov za neparnost:
// i % 2 = 1

// 1.7 ++ operator Increment (Povecava vrednost za 1);
// postfiksni operator
let x = 5;
x++; // x = x + 1
console.log(x);
// console.log(5 ++); // 5 = 5+1 sto nema veze sa vezom

let y = 11;
// prefiksni operator
console.log(++y);

// 1.8  -- operator Decrement (Smanjuje vrednost za 1);
let z = 99;
z--; // z = z-1
// postfiksni perator
console.log(z);
// prefiksni operator
let w = 65;
console.log(--w);

